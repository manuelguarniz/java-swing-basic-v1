    private void cargarDatos() {
        if (gestor.clientes != null) {
            cboClientes.addItem("-- Seleccionar -- ");
            for (Cliente cliente : gestor.clientes) {
                cboClientes.addItem(cliente.getNombre() + " " + cliente.getApellidos());
            }
        }
        if (gestor.autos != null) {
            cboAuto.addItem("-- Seleccionar -- ");
            for (Auto auto : gestor.autos) {
                cboAuto.addItem(auto.getMarca() + " - " + auto.getModelo());
            }
        }

        if (gestor.ventas != null) {
            int index = 0;
            for (Venta venta : gestor.ventas) {
                index++;
                DefaultTableModel tableModel = (DefaultTableModel) jtVentas.getModel();
                tableModel.addRow(new Object[]{
                    venta,
                    index,
                    venta.getCliente().getNombre() + " " + venta.getCliente().getApellidos(),
                    venta.getAuto().getMarca() + " - " + venta.getAuto().getModelo(),
                    venta.getAuto().getPrecio(),});
            }
        }
    }

    private void limpiarDatos() {
        DefaultTableModel tableModel = (DefaultTableModel) jtVentas.getModel();

        cboClientes.removeAllItems();
        cboAuto.removeAllItems();
        tableModel.setRowCount(0);
        
        txtMarca.setText("");
        txtModelo.setText("");
        txtPrecio.setText("");

        txtNombres.setText("");
        txtApellidos.setText("");
        txtFechaNacimiento.setText("");
        txtEdad.setText("");
    }

    private void mostrarCliente() {
        UICliente cliente = new UICliente(detectarCerrado);
        cliente.setVisible(true);
        cliente.setLocationRelativeTo(null);
    }

    private void mostrarAuto() {
        UIAuto cliente = new UIAuto(detectarCerrado);
        cliente.setVisible(true);
        cliente.setLocationRelativeTo(null);
    }

    private void actualizarDatos() {
        limpiarDatos();
        cargarDatos();
    }

    private void registrarVenta() {
        String clienteSeleccionado = (String) cboClientes.getSelectedItem();
        String autoSeleccionado = (String) cboAuto.getSelectedItem();

        Cliente cliente = gestor.buscarCliente(clienteSeleccionado);
        Auto auto = gestor.buscarAuto(autoSeleccionado);

        if (cliente == null && auto == null) {
            JOptionPane.showMessageDialog(this, "Selecciona un cliente y auto");
            return;
        }
        Venta venta = new Venta(auto, cliente);
        gestor.registrar(venta);
        JOptionPane.showMessageDialog(this, "Registro Ã©xitoso !");
        this.actualizarDatos();
    }

    private void seleccionCliente() {
        String clienteSeleccionado = (String)cboClientes.getSelectedItem();
        Cliente cliente = gestor.buscarCliente(clienteSeleccionado);

        if (cliente != null) {
            txtNombres.setText(cliente.getNombre());
            txtApellidos.setText(cliente.getApellidos());
            txtFechaNacimiento.setText(cliente.getFechaNacimientoStr());
            txtEdad.setText(String.valueOf(cliente.getEdad()));
        }

    }

    private void seleccionAuto() {
        String autoSeleccionado = (String)cboAuto.getSelectedItem();
        Auto auto = gestor.buscarAuto(autoSeleccionado);

        if (auto != null) {
            txtMarca.setText(auto.getMarca());
            txtModelo.setText(auto.getModelo());
            txtPrecio.setText(String.valueOf(auto.getPrecio()));
        }

    }

