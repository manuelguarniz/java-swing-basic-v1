    private void guardar() {
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String fechaNacimiento = txtFechaNacimiento.getText();
        Integer edad = Integer.valueOf(txtEdad.getText());
        
        if ("".equals(nombre) || "".equals(apellido) || "".equals(fechaNacimiento)) {
            JOptionPane.showMessageDialog(this, "Falta completar campos !");
            return;
        }
                
        Cliente cliente = new Cliente(nombre, apellido, edad);
        cliente.setFechaNacimiento(fechaNacimiento);
        
        gestor.registrar(cliente);
        JOptionPane.showMessageDialog(this, "Registro Ã©xitoso !");
        detectarCerrado.accept(0);
        this.dispose();
    }
    
    private void calcularEdad() {
        if (!"YYYY-MM-DD".equalsIgnoreCase(txtFechaNacimiento.getText())) {
            String fechaNacimiento = txtFechaNacimiento.getText();
            LocalDate fecha = Util.toDate(fechaNacimiento);

            if (fecha != null) {
                Integer edad = LocalDate.now().compareTo(fecha);
                txtEdad.setText(String.valueOf(edad));
            } else {
               txtFechaNacimiento.setText("YYYY-MM-DD");
            }
        }
    }
    
    private void quitarPlaceholder() {
        txtFechaNacimiento.setText("");
    }

