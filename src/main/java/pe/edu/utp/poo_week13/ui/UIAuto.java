    private void guardar() {
        String marca = txtMarca.getText();
        String modelo = txtModelo.getText();
        Double precio = Util.tryParseDouble(txtPrecio.getText());
        
        if ("".equals(marca) || "".equals(modelo)) {
            JOptionPane.showMessageDialog(this, "Falta completar campos !");
            return;
        }
        
        Auto auto = new Auto(modelo, marca, precio);
        gestor.registrar(auto);
        JOptionPane.showMessageDialog(this, "Registro Ã©xitoso !");
        this.detectarCerrado.accept(0);
        this.dispose();
    }

